print("Nasobici funkce 2*3: "+str(nasobici_funkce(2, 3)))
print("Linearni funkce 2, 3, 4: "+str(linearni_funkce(2,3,4)))
print("Konstantni funkce: "+str(konstantni_funkce_peti()))



